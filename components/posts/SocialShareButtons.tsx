export default function SocialShareButtons({ title, url, excerpt }: SocialShareButtonsProps) {
  // Validate URL - ensure it's a valid, absolute URL
  if (!url || typeof url !== 'string') {
    console.error('SocialShareButtons: Invalid URL provided', url);
    return null;
  }

  // Ensure URL is absolute (starts with http:// or https://)
  const validUrl = url.startsWith('http://') || url.startsWith('https://')
    ? url
    : `https://${url}`;

  const encodedUrl = encodeURIComponent(validUrl);
  const encodedTitle = encodeURIComponent(title);
  const encodedText = encodeURIComponent(excerpt || title);

  const shareLinks = {
    twitter: `https://twitter.com/intent/tweet?url=${encodedUrl}&text=${encodedText}`,
    facebook: `https://www.facebook.com/sharer/sharer.php?u=${encodedUrl}`,
    linkedin: `https://www.linkedin.com/sharing/share-offsite/?url=${encodedUrl}`,
    reddit: `https://reddit.com/submit?url=${encodedUrl}&title=${encodedTitle}`,
  };

  const handleCopyLink = async () => {
    try {
      await navigator.clipboard.writeText(url);
      // TODO: Replace with a toast notification component
      alert('Link copied to clipboard!');
    } catch (err) {
      // Silently fail - clipboard API may not be available in some contexts
      if (process.env.NODE_ENV === 'development') {
      console.error('Failed to copy link:', err);
      }
    }
  };

  const handleInstagramShare = async () => {
    // Instagram doesn't have a web share URL, so we copy the link
    // User can then paste it into Instagram
    try {
      await navigator.clipboard.writeText(url);
      // TODO: Replace with a toast notification component
      alert('Link copied! Paste it into your Instagram post.');
    } catch (err) {
      if (process.env.NODE_ENV === 'development') {
      console.error('Failed to copy link:', err);
      }
    }
  };

  return (
    <div className="flex flex-wrap items-center gap-3 mt-8 pt-8">
      <span className="text-sm font-medium text-[var(--foreground-low)]">Share:</span>
      <div className="flex flex-wrap gap-3">
        <a
          href={shareLinks.twitter}
          target="_blank"
          rel="noopener noreferrer"
          className="text-white hover:text-[var(--electric-blue)] transition-colors duration-300"
          aria-label="Share on Twitter"
        >
          <TwitterIcon />
        </a>
        <a
          href={shareLinks.facebook}
          target="_blank"
          rel="noopener noreferrer"
          className="text-white hover:text-[var(--electric-blue)] transition-colors duration-300"
          aria-label="Share on Facebook"
        >
          <FacebookIcon />
        </a>
        <a
          href={shareLinks.linkedin}
          target="_blank"
          rel="noopener noreferrer"
          className="text-white hover:text-[var(--electric-blue)] transition-colors duration-300"
          aria-label="Share on LinkedIn"
        >
          <LinkedInIcon />
        </a>
        <a
          href={shareLinks.reddit}
          target="_blank"
          rel="noopener noreferrer"
          className="text-white hover:text-[var(--electric-blue)] transition-colors duration-300"
          aria-label="Share on Reddit"
        >
          <RedditIcon />
        </a>
        <button
          onClick={handleInstagramShare}
          className="text-white hover:text-[var(--electric-blue)] transition-colors duration-300"
          aria-label="Share on Instagram"
        >
          <InstagramIcon />
        </button>
        <button
          onClick={handleCopyLink}
          className="text-white hover:text-[var(--electric-blue)] transition-colors duration-300"
          aria-label="Copy link"
        >
          <CopyIcon />
        </button>
      </div>
    </div>
  );
}
